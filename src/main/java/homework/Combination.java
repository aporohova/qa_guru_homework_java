package homework;

public class Combination {
    public static void main(String[] args) {
        System.out.println("Combinations");
        int a = 10;
        double b = 1.5;
        char c = '%';
        byte d = 1;
        String e = "Discount";

        System.out.println("Sum int a and double b = " + (a + b));
        System.out.println("Multiplication int a and double b = " + (a * b));
        System.out.println("Sum int a and char c = " + a + c);
        System.out.println("Sum byte d and int a = " + (d + a));
        System.out.println("Sum String e, int a and char c = " + e + a + c);

    }
}
