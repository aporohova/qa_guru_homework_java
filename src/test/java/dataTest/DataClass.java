package dataTest;

import java.util.Scanner;

public class DataClass {
    public static void main(String[] args) {
        int x = 10;
        int y = 15;
        int z = x + y;
        //Арифметические операторы
        System.out.println("Arithmetical operators");
        System.out.println("sum x + y = " + (x + y));
        System.out.println("subtraction x + y - 5 = " + (x + y - 5));
        System.out.println("multiplication x * y * z = " + (x * y * z));
        System.out.println("division z / x = " + (z / x));
        System.out.println("division with remainder z % y = " + (z % y));
    }
}
